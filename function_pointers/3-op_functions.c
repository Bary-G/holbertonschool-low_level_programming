#include "3-calc.h"


/**
 * op_add - add two num
 * @a: num1
 * @b: num2
 *
 * Return: the result of a+b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two num
 * @a: num1
 * @b: num2
 *
 * Return: the result of a-b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two num
 * @a: num1
 * @b: num2
 *
 * Return: the result of a*b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two num
 * @a: num1
 * @b: num2
 *
 * Return: the result of a/b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - get modulo of two num
 * @a: num1
 * @b: num2
 *
 * Return: the result of a%b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
